package Chapter3_HelloWorld_Integer_VariableNames;

public class DataType_Boolean_3 {
    public static void main(String[] args) {
        boolean flag = true;
        System.out.println(flag);


        byte range;
        range=124;
        System.out.println(124);


        short  temperature;
        temperature= -200;
        System.out.println(temperature);


        int  ranges;
        ranges= -425000;
        System.out.println(ranges);


        long  ranges1=  -4352352532535253L;
        System.out.println(ranges1);

        double currency= -42.3;
        System.out.println(currency);

        float  numbers= -42.3f;
        System.out.println(numbers);

        char letter ='\u0051';
        System.out.println(letter);



    }
}