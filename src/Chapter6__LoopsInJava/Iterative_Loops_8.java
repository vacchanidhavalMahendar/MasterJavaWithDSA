package Chapter6__LoopsInJava;
//Do while loop
public class Iterative_Loops_8 {
    public static void main(String[] args) {

        int max=25;
        for(int i=15;i<=max;i++){
            System.out.println("Hello" +i);
        }

        for(int m=10;m<=9;m++){
            System.out.println("Hello Dhaval");

        }
        int m=10;
        do{
            System.out.println("Hello");
            m++;
        }
        while(m<=9);
    }

}
